package _java._se._01._operators;

public class SwitchWithoutBreak {
    public static void main(String[] args) {
        int x = 10;
        switch(x) {
            case 20:
                System.out.println("20");
            case 30:
                System.out.println("30");
            case 10:
                System.out.println("10");
            case 40:
                System.out.println("40");
            default:
        }
    }

}
